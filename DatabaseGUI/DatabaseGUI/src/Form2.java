    /**
     * Enters in data from the team and coach tables into the text fields of the GUI
     */
    private void populateTextFields() {
    	try {
			Class.forName("com.mysql.cj.jdbc.Driver");
			Connection con = DriverManager.getConnection("jdbc:mysql://localhost/BasketballDatabase?user=root&password=ics311");
	    	Statement mySt = con.createStatement();
	    	ResultSet myRS = mySt.executeQuery("select * from team natural join coach where coach_id = " + count);
	    	while(myRS.next()) {
	    		jTextField14.setText(myRS.getString("team_name"));
	    		jTextField20.setText(myRS.getString("gm_name"));
	    		jTextField21.setText(myRS.getString("t_champ_won"));
	    		jTextField22.setText(myRS.getString("city"));
	    		jTextField23.setText(myRS.getString("state"));
	    		jTextField24.setText(myRS.getString("stadium"));
	    		jTextField19.setText(myRS.getString("coach_id"));
	    		jTextField15.setText(myRS.getString("c_fname"));
	    		jTextField16.setText(myRS.getString("c_lname"));
	    		jTextField17.setText(myRS.getString("c_age"));
	    		jTextField18.setText(myRS.getString("c_champ_won"));
	    		jTextField26.setText(myRS.getString("win_perc"));
	    		jTextField25.setText(myRS.getString("year_exp"));
	    	}
		} catch (Exception ex) {
			ex.printStackTrace();}
    }

