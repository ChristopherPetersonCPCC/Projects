    /**
     * Enters in data from the team and coach tables into the text fields of the GUI
     */
    private void populateTextFields() {
    	try {
			Class.forName("com.mysql.cj.jdbc.Driver");
			Connection con = DriverManager.getConnection("jdbc:mysql://localhost/BasketballDatabase?user=root&password=ics311");
	    	Statement mySt = con.createStatement();
	    	ResultSet myRS = mySt.executeQuery("select * from player where player_ID = " + count);
	    	while(myRS.next()) {
	    		jTextField6.setText(myRS.getString("Player_ID"));
	    		jTextField7.setText(myRS.getString("P_fname"));
	    		jTextField8.setText(myRS.getString("P_lname"));
	    		jTextField9.setText(myRS.getString("p_age"));
	    		jTextField10.setText(myRS.getString("p_height"));
	    		jTextField11.setText(myRS.getString("agent_id"));
	    		jTextField12.setText(myRS.getString("team_name"));
	    		jTextField1.setText(myRS.getString("ppg"));
	    		jTextField2.setText(myRS.getString("rpg"));
	    		jTextField3.setText(myRS.getString("three_pt_perc"));
	    		jTextField4.setText(myRS.getString("dfrtg"));
	    		jTextField5.setText(myRS.getString("p_champ_won"));
	    		jTextField13.setText(myRS.getString("pos"));
	    	}
		} catch (Exception ex) {
			ex.printStackTrace();}}
 

